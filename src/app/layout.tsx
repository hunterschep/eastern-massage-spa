export const viewport: Viewport = {
  themeColor: "#3A4D3F",
};

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const openingHoursSpecification = site.hoursConfirmed
    ? site.hours.flatMap((entry) =>
        "opens" in entry && "closes" in entry
          ? entry.days.map((day) => ({
              "@type": "OpeningHoursSpecification",
              dayOfWeek: day,
              opens: entry.opens,
              closes: entry.closes,
            }))
          : []
      )
    : undefined;

  const serviceCatalog = site.services.map((service) => ({
    "@type": "OfferCatalog",
    name: service.category,
    itemListElement: service.items.map((item) => {
      const numericPriceValue = item.price.replace(/[^0-9.]/g, "");
      const numericPrice = numericPriceValue ? Number(numericPriceValue) : NaN;
      const offer: Record<string, unknown> = {
        "@type": "Offer",
        name: `${service.category} - ${item.name}`,
        description: publicText(service.description, service.category),
      };

      if (!Number.isNaN(numericPrice)) {
        offer.price = numericPrice;
        offer.priceCurrency = "USD";
      }

      return offer;
    }),
  }));

  const addOnCatalog = {
    "@type": "OfferCatalog",
    name: "Massage Enhancements",
    itemListElement: site.addons.map((addon) => {
      const numericPriceValue = addon.price.replace(/[^0-9.]/g, "");
      const numericPrice = numericPriceValue ? Number(numericPriceValue) : NaN;
      const offer: Record<string, unknown> = {
        "@type": "Offer",
        name: addon.name,
        description: publicText(addon.description, addon.name),
      };

      if (!Number.isNaN(numericPrice)) {
        offer.price = numericPrice;
        offer.priceCurrency = "USD";
      }

      return offer;
    }),
  };

  const structuredData = [
    {
      "@context": "https://schema.org",
      "@type": "WebSite",
      "@id": `${site.url}/#website`,
      url: site.url,
      name: site.name,
      description: site.description,
    },
    {
      "@context": "https://schema.org",
      "@type": ["HealthAndBeautyBusiness", "Spa"],
      "@id": `${site.url}/#business`,
      name: site.name,
      legalName: site.legalName,
      url: site.url,
      mainEntityOfPage: site.url,
      description: site.description,
      image: [getAbsoluteUrl(site.images.og), getAbsoluteUrl(site.images.hero)],
      logo: getAbsoluteUrl(site.images.logo),
      telephone: site.phoneE164,
      priceRange: site.priceRange,
      address: {
        "@type": "PostalAddress",
        streetAddress: site.address.streetAddress,
        addressLocality: site.address.addressLocality,
        addressRegion: site.address.addressRegion,
        postalCode: site.address.postalCode,
        addressCountry: site.address.addressCountry,
      },
      geo: {
        "@type": "GeoCoordinates",
        latitude: site.geo.latitude,
        longitude: site.geo.longitude,
      },
      hasMap: site.mapUrl,
      ...(openingHoursSpecification ? { openingHoursSpecification } : {}),
      areaServed: {
        "@type": "City",
        name: site.address.addressLocality,
      },
      sameAs: [site.mapUrl, ...getOfficialProfileUrls()],
      hasOfferCatalog: [...serviceCatalog, addOnCatalog],
      potentialAction: {
        "@type": "ReserveAction",
        target: site.bookingUrl,
      },
    },
  ];

  return (
    <html lang="en" className="scroll-smooth">
      <body className="bg-background text-foreground antialiased">
        <Analytics />
        <JsonLd id="site-structured-data" data={structuredData} />
        {children}
        <StickyMobileCTA />
      </body>
    </html>
  );
}
