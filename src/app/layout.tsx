import JsonLd from "@/components/JsonLd";
import StickyMobileCTA from "@/components/StickyMobileCTA";
import { getAbsoluteUrl, getOfficialProfileUrls, site } from "@/data/site";
import { publicText } from "@/utils/publicContent";
import "./globals.css";

